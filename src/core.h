static unsigned char display_error(const char* error_msg) {
  	char buffer[128];
		sprintf(buffer, error_msg, SDL_GetError());
		SDL_ShowSimpleMessageBox(SDL_MESSAGEBOX_ERROR, "Error!", buffer, NULL);
    return 1;
}

void audio_callback(void *userdata, Uint8 *stream, int len) {
    renderer *core = (renderer *)userdata;
    SDL_memset(stream, 0, len);
    
    for (int i = 0; i < MAX_SOUNDS; i++) {
        wav_sound *s = core->mixer.sounds[i];
        if (!s || !s->playing) continue;

        unsigned remaining = s->length - (s->pos - s->buffer);
        unsigned to_copy = (len > remaining) ? remaining : len;

        SDL_MixAudioFormat(stream, s->pos, core->mixer.spec.format, to_copy, SDL_MIX_MAXVOLUME);
        s->pos += to_copy;

        if (s->pos - s->buffer >= s->length) {
            s->pos = s->buffer;
            s->playing = 0;
        }
    }
}

wav_sound *load_wav(const char *filename, renderer *core) {
    wav_sound *sound = malloc(sizeof(wav_sound));
    if (!sound) return NULL;

    SDL_AudioSpec file_spec;
    Uint8 *buf; Uint32 len;

    if (SDL_LoadWAV(filename, &file_spec, &buf, &len) == NULL) {
        free(sound);
        return NULL;
    }

    SDL_AudioCVT cvt;
    if (SDL_BuildAudioCVT(&cvt,
        file_spec.format, file_spec.channels, file_spec.freq,
        core->mixer.spec.format, core->mixer.spec.channels, core->mixer.spec.freq) > 0)
    {
        cvt.buf = malloc(len * cvt.len_mult);
        cvt.len = len;
        SDL_memcpy(cvt.buf, buf, len);
        SDL_ConvertAudio(&cvt);
        SDL_FreeWAV(buf);
        sound->buffer = cvt.buf;
        sound->length = cvt.len_cvt;
        sound->converted = 1;
    } else {
        sound->converted = 0;
        sound->buffer = buf;
        sound->length = len;
    }

    sound->pos = sound->buffer;
    sound->playing = 0;

    for (int i = 0; i < MAX_SOUNDS; i++) {
        if (!core->mixer.sounds[i]) {
            core->mixer.sounds[i] = sound;
            break;
        }
    }

    return sound;
}

void play_wav(wav_sound *sound, renderer *core) {
    SDL_LockAudioDevice(core->mixer.device);
    sound->pos = sound->buffer;
    sound->playing = 1;
    SDL_UnlockAudioDevice(core->mixer.device);
}

void free_wav(wav_sound *sound, renderer *core) {
    SDL_LockAudioDevice(core->mixer.device);
    for (int i = 0; i < MAX_SOUNDS; i++) {
        if (core->mixer.sounds[i] == sound) {
            core->mixer.sounds[i] = NULL;
            break;
        }
    }
    
    SDL_UnlockAudioDevice(core->mixer.device);
    if (!sound->converted) SDL_FreeWAV(sound->buffer);
    else free(sound->buffer);
    free(sound);
}

unsigned char init_audio(renderer *core) {
    SDL_memset(&core->mixer, 0, sizeof(core->mixer));
    core->mixer.spec.freq = 44100;
    core->mixer.spec.format = AUDIO_S16SYS;
    core->mixer.spec.channels = 2;
    core->mixer.spec.samples = 512;
    core->mixer.spec.userdata = core; 
    core->mixer.spec.callback = audio_callback;

    core->mixer.device = SDL_OpenAudioDevice(NULL, 0, &core->mixer.spec, &core->mixer.spec, 0);
    if (core->mixer.device == 0) return 1;

    SDL_PauseAudioDevice(core->mixer.device, 0);
    return 0;
}

void shutdown_audio(renderer *core) {
    SDL_CloseAudioDevice(core->mixer.device);
    for (int i = 0; i < MAX_SOUNDS; i++) {
        if (core->mixer.sounds[i]) free_wav(core->mixer.sounds[i], core);
    }
}

static char global_scale = 0;

unsigned char init_renderer(renderer* core, char vsync, char scale, const char* name) {
	if (SDL_Init(SDL_INIT_TIMER | SDL_INIT_VIDEO | SDL_INIT_EVENTS | SDL_INIT_AUDIO)) 
    return display_error("SDL2 couldn't initialize!\nError: %s");

  if (init_audio(core)) 
    return display_error("Audio device couldn't be opened!\nError: %s");
	
  global_scale = scale;

	SDL_ShowCursor(1);
	SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
	SDL_SetHint(SDL_HINT_APP_NAME, name);
	SDL_SetHint(SDL_HINT_RENDER_BATCHING, "1");
	SDL_SetHint(SDL_HINT_GRAB_KEYBOARD, "1");
	if (!vsync) SDL_SetHint(SDL_HINT_RENDER_VSYNC, "0");

	core->window = SDL_CreateWindow(name, SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
		WIDTH * TILE_W * scale, HEIGHT * TILE_H * scale, 0);
	if (core->window == NULL) 
    return display_error("Window couldn't be created!\nError: %s");

	core->renderer = SDL_CreateRenderer(core->window, -1, SDL_RENDERER_PRESENTVSYNC | SDL_RENDERER_ACCELERATED);

	if (core->renderer == NULL) {
    core->renderer = SDL_CreateRenderer(core->window, -1, SDL_RENDERER_SOFTWARE);

    if (core->renderer == NULL) return display_error("Renderer couldn't be created!\nError: %s");
  }

	SDL_RenderSetScale(core->renderer, scale, scale);
	
	SDL_Surface* raw_gfx = SDL_CreateRGBSurfaceFrom((void*)font.pixel_data, font.width, font.height,
		font.bytes_per_pixel * 8, font.bytes_per_pixel * font.width, 0x000000ff, 0x0000ff00, 0x00ff0000,
		(font.bytes_per_pixel == 3) ? 0 : 0xff000000);

	if (raw_gfx == NULL) 
    return display_error("Font couldn't be loaded!\nError: %s");

	core->gfx = SDL_CreateTextureFromSurface(core->renderer, raw_gfx); SDL_FreeSurface(raw_gfx);
	if (core->gfx == NULL) 
    return display_error("Font couldn't initialized!\nError: %s");

	return 0;
}

void draw_tile_camera(renderer* core, char symbol, const unsigned char* color, signed x, signed y, signed cx, signed cy) {
  long real_x = x + WIDTH / 2 - cx, real_y = y + HEIGHT / 2 - cy;
  if (real_x < WIDTH && real_x >= 0 && real_y < HEIGHT && real_y >= 0) {
    SDL_Rect part;
    part.x = TILE_W * (symbol - 32); part.y = 0;
    part.w = TILE_W; part.h = TILE_H;

    SDL_Rect pos;
    pos.x = real_x * TILE_W; pos.y = real_y * TILE_H;
    pos.w = TILE_W; pos.h = TILE_H;

    SDL_SetTextureColorMod(core->gfx, color[0], color[1], color[2]);
    SDL_RenderCopy(core->renderer, core->gfx, &part, &pos);
  }
}

void draw_tile(renderer* core, char symbol, const unsigned char* color, signed x, signed y) {
  if (x < WIDTH && x >= 0 && y < HEIGHT && y >= 0) {
    SDL_Rect part;
    part.x = TILE_W * (symbol - 32); part.y = 0;
    part.w = TILE_W; part.h = TILE_H;

    SDL_Rect pos;
    pos.x = x * TILE_W; pos.y = y * TILE_H;
    pos.w = TILE_W; pos.h = TILE_H;

    SDL_SetTextureColorMod(core->gfx, color[0], color[1], color[2]);
    SDL_RenderCopy(core->renderer, core->gfx, &part, &pos);
  }
}

void print(renderer* core, const char* text, const unsigned char* color, signed x, signed y) {
  for (int i = 0; i < strlen(text); i++) {
    draw_tile(core, text[i], color, x + i, y);
  }
}

void clear_screen(renderer* core) {
	SDL_RenderClear(core->renderer);
}

double frame_time = 0.0;

#ifdef SHOW_FPS
float fps = 0.0f;
int frame_count = 0;
float time_accumulator = 0.0f;
#endif

void run_render(renderer* core) {
	SDL_Event e; char quit = 0;

	while (!quit) {
	unsigned long long start = SDL_GetPerformanceCounter();

		while (SDL_PollEvent(&e)) { 
			if (e.type == SDL_QUIT) quit = 1; 
			if (e.type == SDL_KEYDOWN) keyboard_game(e.key.keysym.scancode, core);
		}

    signed x, y;
    int mouse_buttons = SDL_GetMouseState(&x, &y);

    if (mouse_buttons & SDL_BUTTON(SDL_BUTTON_LEFT)) mouse_buttons = 1;
    else if (mouse_buttons & SDL_BUTTON(SDL_BUTTON_RIGHT)) mouse_buttons = 2;
    else if (mouse_buttons & SDL_BUTTON(SDL_BUTTON_MIDDLE)) mouse_buttons = 3;
    else mouse_buttons = 0;

    #ifndef FLASH
		SDL_RenderClear(core->renderer);
    #endif

    mouse_game(core, x / (signed)global_scale / TILE_W, y / (signed)global_scale / TILE_H, mouse_buttons);
		update_game(core, (float)frame_time);

		SDL_RenderPresent(core->renderer);

    unsigned long long end = SDL_GetPerformanceCounter();
    frame_time = (end - start) / (double)SDL_GetPerformanceFrequency();
		
    #ifdef SHOW_FPS
		frame_count++;
    	time_accumulator += frame_time;

	    if (time_accumulator >= 1.0f) {
	        fps = frame_count / time_accumulator;
	        frame_count = 0;
	        time_accumulator = 0.0f;
	    }
		
      char buffer[128]; sprintf(buffer, "Current FPS: %d", (int)fps);
      SDL_SetWindowTitle(core->window, buffer);
    #endif
	}
}


void shutdown_renderer(renderer* core) {
	shutdown_game();
  shutdown_audio(core);
	SDL_DestroyTexture(core->gfx);
	SDL_DestroyRenderer(core->renderer);
	SDL_DestroyWindow(core->window);
	SDL_Quit();
}

