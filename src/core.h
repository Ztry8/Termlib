typedef struct {
    unsigned char *buffer;
    unsigned length;
    unsigned char *pos;
    char playing;
} wav_sound;

typedef struct Core {
	SDL_Window* window;     
	SDL_Renderer* renderer; 
	SDL_Texture* gfx;     
  SDL_AudioSpec* spec;
} renderer;

void input_game(SDL_Scancode, renderer*);
void update_game(renderer*);
void shutdown_game(void);

static unsigned char display_error(const char* error_msg) {
  	char buffer[128];
		sprintf(buffer, error_msg, SDL_GetError());
		SDL_ShowSimpleMessageBox(SDL_MESSAGEBOX_ERROR, "Error!", buffer, NULL);
    return 1;
}

void audio_callback(void *userdata, Uint8 *stream, int len) {
    wav_sound *sound = (wav_sound *)userdata;

    if (!sound->playing) {
        SDL_memset(stream, 0, len);
        return;
    }

    unsigned remaining = sound->length - (sound->pos - sound->buffer);
    unsigned to_copy = (len > remaining) ? remaining : len;

    SDL_memcpy(stream, sound->pos, to_copy);
    sound->pos += to_copy;

    if (sound->pos - sound->buffer >= sound->length) {
      sound->pos = sound->buffer; 
      sound->playing = 0;
    }

    if (to_copy < len) {
        SDL_memset(stream + to_copy, 0, len - to_copy);
    }
}

wav_sound *load_wav(const char *filename, renderer* core) {
    wav_sound *sound = malloc(sizeof(wav_sound));

    if (!sound || SDL_LoadWAV(filename, core->spec, &sound->buffer, &sound->length) == NULL) {
        display_error("Sound couldn't be loaded!\nError: %s");
        free(sound);
    }

    sound->playing = 0;
    sound->pos = sound->buffer;
    core->spec->callback = audio_callback;
    core->spec->userdata = sound;

    return sound;
}

void play_wav(wav_sound *sound, renderer* core) {
    if (SDL_OpenAudio(core->spec, NULL) < 0) {
        display_error("Sound couldn't be played!\nError: %s");
        return;
    }
    
    SDL_PauseAudio(0); 
    while (sound->playing) {
        SDL_Delay(1);
    }

    sound->playing = 1;
    SDL_CloseAudio();
}

unsigned char init_renderer(renderer* core, char vsync, char scale, const char* name) {
	if (SDL_Init(SDL_INIT_TIMER | SDL_INIT_VIDEO | SDL_INIT_EVENTS | SDL_INIT_AUDIO)) 
    return display_error("SDL2 couldn't initialize!\nError: %s");
	
	SDL_ShowCursor(1);
	SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
	SDL_SetHint(SDL_HINT_APP_NAME, name);
	SDL_SetHint(SDL_HINT_RENDER_BATCHING, "1");
	SDL_SetHint(SDL_HINT_GRAB_KEYBOARD, "1");
	if (!vsync) SDL_SetHint(SDL_HINT_RENDER_VSYNC, "0");

	core->window = SDL_CreateWindow(name, SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
		WIDTH * TILE_W * scale, HEIGHT * TILE_H * scale, 0);
	if (core->window == NULL) 
    return display_error("Window couldn't be created!\nError: %s");

	core->renderer = SDL_CreateRenderer(core->window, -1, SDL_RENDERER_PRESENTVSYNC | SDL_RENDERER_ACCELERATED);
	if (core->renderer == NULL) 
    return display_error("Renderer couldn't be created!\nError: %s");

	SDL_RenderSetScale(core->renderer, scale, scale);
	
	SDL_Surface* raw_gfx = SDL_CreateRGBSurfaceFrom((void*)font.pixel_data, font.width, font.height,
		font.bytes_per_pixel * 8, font.bytes_per_pixel * font.width, 0x000000ff, 0x0000ff00, 0x00ff0000,
		(font.bytes_per_pixel == 3) ? 0 : 0xff000000);

	if (raw_gfx == NULL) 
    return display_error("Font couldn't be loaded!\nError: %s");

	core->gfx = SDL_CreateTextureFromSurface(core->renderer, raw_gfx); SDL_FreeSurface(raw_gfx);
	if (core->gfx == NULL) 
    return display_error("Font couldn't initialized!\nError: %s");

	return 0;
}

void draw_tile_camera(renderer* core, char symbol, const unsigned char* color, long x, long y, long cx, long cy) {
  long real_x = x + WIDTH / 2 - cx, real_y = y + HEIGHT / 2 - cy;
  if (real_x < WIDTH && real_x >= 0 && real_y < HEIGHT && real_y >= 0) {
    SDL_Rect part;
    part.x = TILE_W * (symbol - 32); part.y = 0;
    part.w = TILE_W; part.h = TILE_H;

    SDL_Rect pos;
    pos.x = real_x * TILE_W; pos.y = real_y * TILE_H;
    pos.w = TILE_W; pos.h = TILE_H;

    SDL_SetTextureColorMod(core->gfx, color[0], color[1], color[2]);
    SDL_RenderCopy(core->renderer, core->gfx, &part, &pos);
  }
}

void draw_tile(renderer* core, char symbol, const unsigned char* color, long x, long y) {
  if (x < WIDTH && x >= 0 && y < HEIGHT && y >= 0) {
    SDL_Rect part;
    part.x = TILE_W * (symbol - 32); part.y = 0;
    part.w = TILE_W; part.h = TILE_H;

    SDL_Rect pos;
    pos.x = x * TILE_W; pos.y = y * TILE_H;
    pos.w = TILE_W; pos.h = TILE_H;

    SDL_SetTextureColorMod(core->gfx, color[0], color[1], color[2]);
    SDL_RenderCopy(core->renderer, core->gfx, &part, &pos);
  }
}

void print(renderer* core, const char* text, const unsigned char* color, long x, long y) {
  for (int i = 0; i < strlen(text); i++) {
    draw_tile(core, text[i], color, x + i, y);
  }
}

void run_render(renderer* core) {
	SDL_Event e; char quit = 0;
	while (!quit) {
    #ifdef SHOW_FPS
		  unsigned long long start = SDL_GetPerformanceCounter();
    #endif
		while (SDL_PollEvent(&e)) { 
			if (e.type == SDL_QUIT) quit = 1; 
			if (e.type == SDL_KEYDOWN) input_game(e.key.keysym.scancode, core);
		}

		SDL_RenderClear(core->renderer);
		update_game(core);
		SDL_RenderPresent(core->renderer);

    #ifdef SHOW_FPS
      unsigned long long end = SDL_GetPerformanceCounter();
      float elapsed = 1.f / ((end - start) / (float)SDL_GetPerformanceFrequency());
      char buffer[128]; sprintf(buffer, "Current FPS: %0.0f", elapsed);
      SDL_SetWindowTitle(core->window, buffer);
    #endif
	}
}


void shutdown_renderer(renderer* core) {
	shutdown_game();
	SDL_DestroyTexture(core->gfx);
	SDL_DestroyRenderer(core->renderer);
	SDL_DestroyWindow(core->window);
	SDL_Quit();
}
